function formatDateStr(date) {
  if (!date) return null
  const d = date instanceof Date ? date : new Date(date)
  const year = d.getFullYear()
  const month = String(d.getMonth() + 1).padStart(2, '0')
  const day = String(d.getDate()).padStart(2, '0')
  return `${year}-${month}-${day}`
}

function getYearConfig(year) {
  const yearNum = typeof year === 'number' ? year : new Date(year).getFullYear()
  return CHINESE_HOLIDAYS[yearNum] || null
}

function isHoliday(dateStr) {
  if (!dateStr) return false
  const date = new Date(dateStr)
  const year = date.getFullYear()
  const yearConfig = getYearConfig(year)
  if (!yearConfig) {
    return year >= 2026
  }
  const dateStrFormatted = formatDateStr(dateStr)
  for (const holiday of yearConfig.holidays) {
    if (holiday.dates.includes(dateStrFormatted)) {
      return true
    }
  }
  return false
}

function isWorkday(dateStr) {
  if (!dateStr) return false
  const date = new Date(dateStr)
  const year = date.getFullYear()
  const yearConfig = getYearConfig(year)
  if (!yearConfig) {
    return year >= 2026
  }
  const dateStrFormatted = formatDateStr(dateStr)
  return yearConfig.workdays.includes(dateStrFormatted)
}

function isWeekend(dateStr) {
  if (!dateStr) return false
  const date = new Date(dateStr)
  const day = date.getDay()
  return day === 0 || day === 6
}

function isNonWorkingDay(dateStr) {
  if (isWorkday(dateStr)) return false
  const date = new Date(dateStr)
  if (isWeekend(date)) return true
  if (isHoliday(dateStr)) return true
  return false
}

function getWorkDaysInRange(startDate, endDate) {
  if (!startDate || !endDate) return 0
  const start = new Date(startDate)
  const end = new Date(endDate)
  start.setHours(0, 0, 0, 0)
  end.setHours(0, 0, 0, 0)
  let workDays = 0
  const current = new Date(start)
  while (current <= end) {
    if (!isNonWorkingDay(current)) {
      workDays++
    }
    current.setDate(current.getDate() + 1)
  }
  return workDays
}

function getNonWorkingDaysInRange(startDate, endDate) {
  if (!startDate || !endDate) return 0
  const start = new Date(startDate)
  const end = new Date(endDate)
  start.setHours(0, 0, 0, 0)
  end.setHours(0, 0, 0, 0)
  let count = 0
  const current = new Date(start)
  while (current <= end) {
    if (isNonWorkingDay(current)) {
      count++
    }
    current.setDate(current.getDate() + 1)
  }
  return count
}

function getElapsedWorkDays(startDate, referenceDate) {
  if (!startDate || !referenceDate) return 0
  const start = new Date(startDate)
  const ref = new Date(referenceDate)
  start.setHours(0, 0, 0, 0)
  ref.setHours(0, 0, 0, 0)
  if (ref <= start) return 0
  let workDays = 0
  const current = new Date(start)
  while (current < ref) {
    if (!isNonWorkingDay(current)) {
      workDays++
    }
    current.setDate(current.getDate() + 1)
  }
  return workDays
}

function getRemainingWorkDays(startDate, endDate, referenceDate) {
  if (!startDate || !endDate) return 0
  const ref = referenceDate ? new Date(referenceDate) : new Date()
  ref.setHours(0, 0, 0, 0)
  const start = new Date(startDate)
  const end = new Date(endDate)
  start.setHours(0, 0, 0, 0)
  end.setHours(0, 0, 0, 0)
  if (ref >= end) return 0
  const fromDate = ref >= start ? ref : start
  let workDays = 0
  const current = new Date(fromDate)
  while (current < end) {
    current.setDate(current.getDate() + 1)
    if (!isNonWorkingDay(current)) {
      workDays++
    }
  }
  return workDays
}

